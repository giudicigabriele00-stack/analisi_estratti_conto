file_path = file_disponibili[scelta]
excel = openpyxl.load_workbook(file_path)
estratto_conto = excel.active

print(f"Sto analizzando: {file_path}")


#crea foglio analisi se non presente
if 'Foglio_analisi' not in excel.sheetnames:
  foglio_analisi = excel.create_sheet(title='Foglio_analisi')
elif 'Foglio_analisi' in excel.sheetnames:
  foglio_analisi = excel['Foglio_analisi']
  foglio_analisi.delete_cols(1,1000)


#Formattazione del foglio
foglio_analisi.sheet_properties.tabColor = '008000'
foglio_analisi.sheet_view.showGridLines = False

"""Prima funzione che calcola entrate ed uscite: somma tutte le voci con importo negativo come uscite (compresi gli investimenti perchè in genere vengono passati ad un conto diverso) e poi viene effettuata una somma per categoria di uscite"""

entrate = 0
uscite = 0
investimenti = 0

for riga in estratto_conto.iter_rows(min_row=2, values_only=True):
    importo = riga[1]
    if importo > 0:
        entrate += importo
    elif importo < 0:
      if riga[2] == 'Liquidità investita':
        investimenti += importo
      else:
        uscite += importo

foglio_analisi['A1'] = "Entrate totali"
foglio_analisi['B1'] = entrate

foglio_analisi['A2'] = "Uscite totali"
foglio_analisi['B2'] = uscite * (-1)

foglio_analisi['A3'] = "Investimenti"
foglio_analisi['B3'] = investimenti * (-1)

foglio_analisi['A4'] = "Saldo";
foglio_analisi['B4'] = entrate + uscite + investimenti


# Somme parziali per categoria

spese_categoria = {}
for riga in estratto_conto.iter_rows(min_row=2, values_only=True):
    importo = riga[1]
    categoria = riga[2]
    if importo < 0:
        spesa = abs(importo)
        if categoria in spese_categoria:
            spese_categoria[categoria] += spesa
        else:
            spese_categoria[categoria] = spesa

foglio_analisi['A7'] = "Categoria"; foglio_analisi['B7'] = "Spesa"
riga_corrente = 8
for categoria, totale in spese_categoria.items():
    foglio_analisi[f'A{riga_corrente}'] = categoria
    foglio_analisi[f'B{riga_corrente}'] = totale
    riga_corrente += 1

ultima_riga = 7 + len(spese_categoria)

# Grafico a torta

dati_torta = Reference(foglio_analisi, min_col=2, min_row=2, max_row=4)
cat_torta  = Reference(foglio_analisi, min_col=1, min_row=2, max_row=4)

torta = PieChart()
torta.title = "Ripartizione spese"
torta.add_data(dati_torta)
torta.set_categories(cat_torta)
torta.dataLabels = DataLabelList()
torta.dataLabels.showPercent = True
foglio_analisi.add_chart(torta, "D5")

#Grafico a barre verticali
dati_barre = Reference(foglio_analisi, min_col=2, min_row=7, max_row=ultima_riga)
cat_barre  = Reference(foglio_analisi, min_col=1, min_row=8, max_row=ultima_riga)

barre = BarChart()
barre.type = "col"
barre.title = "Spese per categoria"
barre.y_axis.title = 'Euro'
barre.add_data(dati_barre, titles_from_data=True)
barre.set_categories(cat_barre)
foglio_analisi.add_chart(barre, "D25")


excel.save(file_path)

"""Funzione che calcola l'andamento della liquidità giorno per giorno"""

#movimento netto giornaliero
movimento_giorno = {}
for riga in estratto_conto.iter_rows(min_row=2, values_only=True):
    data = riga[0]
    importo = riga[1]
    if data in movimento_giorno:
        movimento_giorno[data] += importo
    else:
        movimento_giorno[data] = importo

#ordina dalla data più lontana alla più recente
date_ordinate = sorted(movimento_giorno)

#ordino estratto conto per data
transazioni = list(estratto_conto.iter_rows(min_row=2, values_only=True))
transazioni_ordinate = sorted(transazioni, key=lambda riga: riga[0])

riga_corrente = 2
for riga in transazioni_ordinate:
    for numero_colonna, valore in enumerate(riga, start=1):
        estratto_conto.cell(row=riga_corrente, column=numero_colonna, value=valore)
    riga_corrente += 1

#calcolo saldo progressivo partendo da 0 nel foglio dell'estratto conto
estratto_conto['F1'] = 'Saldo'

saldo = 0
riga_corrente = 2

for riga in estratto_conto.iter_rows(min_row=2, values_only=True):
    saldo  += riga[1]
    estratto_conto[f'F{riga_corrente}'] = saldo
    riga_corrente += 1

excel.save(file_path)


