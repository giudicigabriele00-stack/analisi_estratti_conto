"""Sistemazione del layout"""

from openpyxl.styles import Font, PatternFill, Border, Side, Alignment
from openpyxl.utils import range_boundaries
from openpyxl.chart.layout import Layout, ManualLayout

GRIGIO="F2F2F3"; BIANCO="FFFFFF"; NAVY="1F3864"; BORDO="A6A6A6"

#sfondo grigio
fill_grigio = PatternFill("solid", fgColor=GRIGIO)
for r in range(1, 61):
    for c in range(1, 20):
        foglio_analisi.cell(r, c).fill = fill_grigio

#larghezza colonne
foglio_analisi.column_dimensions['A'].width = 26
foglio_analisi.column_dimensions['B'].width = 15

#intestazioni
foglio_analisi['A7'] = "Categoria"; foglio_analisi['B7'] = "Spesa"


sottile = Side(style="thin", color=BORDO)
bordo_box = Border(left=sottile, right=sottile, top=sottile, bottom=sottile)

def carta_bianca(rng):                       # riquadro bianco + bordi
    c1, r1, c2, r2 = range_boundaries(rng)
    for r in range(r1, r2+1):
        for c in range(c1, c2+1):
            cell = foglio_analisi.cell(r, c)
            cell.fill = PatternFill("solid", fgColor=BIANCO)
            cell.border = bordo_box

def header_navy(rng):                         # intestazione navy, testo bianco grande
    c1, r1, c2, r2 = range_boundaries(rng)
    for r in range(r1, r2+1):
        for c in range(c1, c2+1):
            cell = foglio_analisi.cell(r, c)
            cell.fill = PatternFill("solid", fgColor=NAVY)
            cell.font = Font(color="FFFFFF", bold=True, size=12)
            cell.border = bordo_box


# Tabella totali
